// 获取人物小传（根据突破层级解锁）
// breakThrough: 0=未突破, 1=1次, 2=2次, ..., 5=5次
export function getCharacterBiography(charId, breakThrough = 0) {
  const bio = characterBiographies[charId]
  if (!bio) return null
  // 第一段：默认解锁
  // 第二段：突破2次解锁（4星10级或更高）
  // 第三段：突破4次解锁（5星30级或更高）
  // 这里简化：breakThrough >= 2 解锁第二段，breakThrough >= 4 解锁第三段
  const parts = [bio.part1]
  if (breakThrough >= 2) parts.push(bio.part2)
  if (breakThrough >= 4) parts.push(bio.part3)
  return {
    part1: bio.part1,
    part2: parts.length > 1 ? bio.part2 : null,
    part3: parts.length > 2 ? bio.part3 : null,
    unlockedCount: parts.length,
    isPart2Locked: breakThrough < 2,
    isPart3Locked: breakThrough < 4
  }
}
